    // =========================================
    // BUTTON KELUAR
    // =========================================
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int baris =
                jTable1.getSelectedRow();

        if (baris == -1) {

            JOptionPane.showMessageDialog(
                    this,
                    "Pilih kendaraan dulu!"
            );

            return;
        }

        try {

            String plat =
                    jTable1.getValueAt(
                            baris,
                            0
                    ).toString();

            String slotParkir =
                    jTable1.getValueAt(
                            baris,
                            2
                    ).toString();

            Connection conn =
                    database.Koneksi.getConnection();

            // ==================================
            // AMBIL DATA
            // ==================================
            String sqlData =
                    "SELECT * FROM transaksi "
                    + "WHERE plat_nomor = ? "
                    + "AND waktu_keluar IS NULL";

            PreparedStatement psData =
                    conn.prepareStatement(sqlData);

            psData.setString(1, plat);

            ResultSet rs =
                    psData.executeQuery();

            int totalBiaya = 0;

            if (rs.next()) {

                LocalDateTime masuk =
                        rs.getTimestamp(
                                "waktu_masuk"
                        ).toLocalDateTime();

                LocalDateTime keluar =
                        LocalDateTime.now();

                long jam =
                        Duration.between(
                                masuk,
                                keluar
                        ).toHours();

                if (jam <= 0) {

                    jam = 1;
                }

                int tarifPerjam =
                        rs.getInt(
                                "tarif_perjam"
                        );

                String jenis =
                        rs.getString(
                                "jenis"
                        );

                int tarifAwal = 0;

                if (jenis.equals("Motor")) {

                    tarifAwal = 2000;

                } else {

                    tarifAwal = 5000;
                }

                totalBiaya =
                        tarifAwal
                        + ((int) jam * tarifPerjam);
            }

            // ==================================
            // UPDATE DATABASE
            // ==================================
            String sql =
                    "UPDATE transaksi "
                    + "SET waktu_keluar = NOW(), "
                    + "total_biaya = ? "
                    + "WHERE plat_nomor = ? "
                    + "AND waktu_keluar IS NULL";

            PreparedStatement pst =
                    conn.prepareStatement(sql);

            pst.setInt(1, totalBiaya);

            pst.setString(2, plat);

            pst.executeUpdate();

            // ==================================
            // SLOT JADI HIJAU
            // ==================================
            int nomorSlot =
                    Integer.parseInt(
                            slotParkir.replace("S", "")
                    ) - 1;

            statusSlot[nomorSlot] = false;

            slot[nomorSlot]
                    .setBackground(Color.GREEN);

            // ==================================
            // HAPUS TABLE
            // ==================================
            DefaultTableModel model =
                    (DefaultTableModel)
                    jTable1.getModel();

            model.removeRow(baris);

            // ==================================
            // UPDATE STATISTIK
            // ==================================
            totalKendaraan--;

            jLabel17.setText(
                    "Total Kendaraan: "
                    + totalKendaraan
            );

            // ==================================
            // RESET UI
            // ==================================
            tfplat.setText("");

            jButton1.setEnabled(true);

            jButton2.setEnabled(false);

            jTable1.clearSelection();

            JOptionPane.showMessageDialog(
                    this,
                    "Kendaraan keluar!"
                    + "\nTotal Biaya: "
                    + totalBiaya
            );

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    "Error: "
                    + e.getMessage()
            );
        }
    }//GEN-LAST:event_jButton2ActionPerformed

