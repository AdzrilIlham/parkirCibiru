    // KENDARAAN MASUK
    // =========================================
    private void kendaraanMasuk() {

        String plat = tfplat.getText();

        String jenis =
                jComboBox1.getSelectedItem().toString();

        // VALIDASI
        if (plat.isEmpty()) {

            JOptionPane.showMessageDialog(
                    this,
                    "Plat nomor harus diisi!"
            );

            return;
        }

        // CARI SLOT KOSONG
        int indexSlot = -1;

        for (int i = 0; i < statusSlot.length; i++) {

            if (!statusSlot[i]) {

                indexSlot = i;

                break;
            }
        }

        // JIKA PENUH
        if (indexSlot == -1) {

            JOptionPane.showMessageDialog(
                    this,
                    "Parkiran penuh!"
            );

            return;
        }

        // SLOT TERISI
        statusSlot[indexSlot] = true;

        slot[indexSlot].setBackground(Color.RED);

        // TARIF
        int tarifAwal = 0;

        int tarifPerjam = 0;

        switch (jenis) {

            case "Motor":

                tarifAwal = 2000;

                tarifPerjam = 1000;

                break;

            case "Mobil":

                tarifAwal = 5000;

                tarifPerjam = 5000;

                break;

            default:

                tarifAwal = 3000;

                tarifPerjam = 1000;

                break;
        }

        String waktuMasuk =
                LocalDateTime.now().toString();

        // UPDATE STATISTIK
        totalKendaraan++;

        totalPemasukan += tarifAwal;

        jLabel17.setText(
                "Total Kendaraan: "
                + totalKendaraan
        );

        // MASUKKAN KE TABLE
        DefaultTableModel model =
                (DefaultTableModel)
                jTable1.getModel();

        model.addRow(new Object[]{
            plat,
            jenis,
            "S" + (indexSlot + 1),
            tarifPerjam,
            waktuMasuk
        });

        // SIMPAN DATABASE
        try {

            Connection conn =
                    database.Koneksi.getConnection();

            String sql =
                    "INSERT INTO transaksi "
                    + "(plat_nomor, jenis, slot, waktu_masuk, tarif_perjam, total_biaya) "
                    + "VALUES (?, ?, ?, NOW(), ?, ?)";

            PreparedStatement pst =
                    conn.prepareStatement(sql);

            pst.setString(1, plat);

            pst.setString(2, jenis);

            pst.setString(3,
                    "S" + (indexSlot + 1));

            pst.setInt(4, tarifPerjam);

            pst.setInt(5, tarifAwal);

            pst.executeUpdate();

            System.out.println(
                    "Data berhasil disimpan!"
            );

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    "Gagal simpan database: "
                    + e.getMessage()
            );
        }

        // RESET INPUT
        tfplat.setText("");

        JOptionPane.showMessageDialog(
                this,
                "Kendaraan masuk ke slot S"
                + (indexSlot + 1)
        );
    }
    
