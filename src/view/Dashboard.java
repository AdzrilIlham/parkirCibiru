private void loadDataParkir() {

        try {

            Connection conn =
                    database.Koneksi.getConnection();

            String sql =
                    "SELECT * FROM transaksi "
                    + "WHERE waktu_keluar IS NULL";

            Statement st =
                    conn.createStatement();

            ResultSet rs =
                    st.executeQuery(sql);

            DefaultTableModel model =
                    (DefaultTableModel)
                    jTable1.getModel();

            model.setRowCount(0);

            totalKendaraan = 0;

            totalPemasukan = 0;

            while (rs.next()) {

                String plat =
                        rs.getString("plat_nomor");

                String jenis =
                        rs.getString("jenis");

                String slotDb =
                        rs.getString("slot");

                int tarifPerjam =
                        rs.getInt("tarif_perjam");

                String waktuMasuk =
                        rs.getString("waktu_masuk");

                model.addRow(new Object[]{
                    plat,
                    jenis,
                    slotDb,
                    tarifPerjam,
                    waktuMasuk
                });

                // NOMOR SLOT
                int nomorSlot =
                        Integer.parseInt(
                                slotDb.replace("S", "")
                        ) - 1;

                statusSlot[nomorSlot] = true;

                slot[nomorSlot]
                        .setBackground(Color.RED);

                // UPDATE STATISTIK
                totalKendaraan++;

                if (jenis.equals("Motor")) {

                    totalPemasukan += 2000;

                } else {

                    totalPemasukan += 5000;
                }
            }

            jLabel17.setText(
                    "Total Kendaraan: "
                    + totalKendaraan
            );


        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    "Gagal load data: "
                    + e.getMessage()
            );
        }
}
    
